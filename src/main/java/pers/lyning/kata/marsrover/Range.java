package pers.lyning.kata.marsrover;

/**
 * @author lyning
 */
public class Range {
    private final Integer x;
    private final Integer y;

    public Range(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
